    private void hienThi() {
        
        ArrayList<NhanVien_POJO> ds =NhanVien_DAO.layDanhSachNhanVien();
        
        DefaultTableModel dtmnv = new DefaultTableModel();
        
        dtmnv.addColumn("Mã nhân viên");
        dtmnv.addColumn("Tên nhân viên");
        dtmnv.addColumn("Địa chỉ");
        dtmnv.addColumn("Năm sinh");
        dtmnv.addColumn("Giới tính");
        
        dtmnv.setNumRows(ds.size());
        for(int i=0;i<ds.size();i++)
        {
            NhanVien_POJO nv = ds.get(i);
            
            dtmnv.setValueAt(nv.getMaNV(), i, 0);
            dtmnv.setValueAt(nv.getTenNV(), i, 1);
            dtmnv.setValueAt(nv.getDiaChi(), i, 2);
            dtmnv.setValueAt(nv.getNamSinh(), i, 3);
            dtmnv.setValueAt(nv.getGioiTinh(), i, 4);
            
        }
        this.tblNhanVien.setModel(dtmnv);
    }
    public boolean checkUsername(String mnv){
        
        
        boolean username_exist = false;
        
        String sql = "SELECT MANV FROM NHUNG.NHANVIEN WHERE MANV = '"+mnv+"' ";
        
        try {
        String strUsername = frmDangNhap.strTenDangNhap;
        String strPassword = frmDangNhap.strMatKhau;
        conn helper = new conn();
        helper.open(strUsername,strPassword);
            
            result = helper.executeQuery(sql);
        
            if(result.next())
            {
                username_exist = true;
            }
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        return username_exist ;
    }

