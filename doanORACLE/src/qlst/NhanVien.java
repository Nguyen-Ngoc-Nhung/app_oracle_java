    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String mnv = txtMaNV.getText().trim();
        String tennv = txtTenNV.getText();
        String diachi = txtDC.getText();
        int namsinh= Integer.parseInt(txtNamSinh.getText());
        String gioitinh = txtGioiTinh.getText();
        
        if(checkUsername(mnv)== true) {
            int ret = JOptionPane.showConfirmDialog(null,
                "Mã ["+txtMaNV.getText()+"] đã tồn tại, bạn có muốn cập nhật không ??",
                "Thông báo",JOptionPane.YES_NO_OPTION);
            if(ret == JOptionPane.NO_OPTION) return;
            try {
                NhanVien_POJO hd = new NhanVien_POJO();
                hd.setTenNV(tennv);
                hd.setDiaChi(diachi);
                hd.setNamSinh(namsinh);
                hd.setGioiTinh(gioitinh);
                hd.setMaNV(mnv);
                
                if (NhanVien_DAO.updateNV(hd)) {
                    JOptionPane.showMessageDialog(this, "Cap nhat nhan vien thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    hienThi();
                } else {
                    JOptionPane.showMessageDialog(this, "Cap nhat nhan vien thất bại", "Thông Báo", JOptionPane.WARNING_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        else{
         try {
           
            NhanVien_POJO hd = new NhanVien_POJO();
            hd.setMaNV(mnv);
            hd.setTenNV(tennv);
            hd.setDiaChi(diachi);
            hd.setNamSinh(namsinh);
            hd.setGioiTinh(gioitinh);

            if (NhanVien_DAO.themNhanVien(hd)) {
                JOptionPane.showMessageDialog(this, "Thêm nhan vien thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                hienThi();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm nhan vien thất bại", "Thông Báo", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi");
        }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

