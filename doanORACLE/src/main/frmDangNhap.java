public void hienThi(){
    
    String sql = "SELECT USERNAME , LAST_LOGIN "
                    + "FROM DBA_USERS WHERE TO_CHAR(LAST_LOGIN,'DD-MM-YYYY HH24:MI:SS') != 'NULL'";
            
        try
        {
            preStatement=conn.prepareStatement(sql);
            result=preStatement.executeQuery();
            dtmUser.setNumRows(0);
            
            while(result.next()){
                Vector<Object> vec=new Vector<>();
                vec.add(result.getString("USERNAME"));
                vec.add(result.getString("LAST_LOGIN"));
                dtmUser.addRow(vec);
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
}
   
