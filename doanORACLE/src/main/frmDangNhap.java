    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        // TODO add your handling code here:
        String strUser = txtTenDangNhap.getText().trim();
        String strPassWord = txtMatKhau.getText().trim();
        
        try{
          if(strUser.equals("sys") == true)
          {
            strUser="sys as sysdba";
          }
          
            conn con = new conn(); 
            bKetQuaDangNhap=con.open(strUser,strPassWord);
            User_DAO d=new User_DAO();
            User_POJO u = d.check(strUser,strPassWord);
            User_POJO u1 = d.check1(strUser);
            if(bKetQuaDangNhap){
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                SharedData.nguoi = u;
                SharedData.nguoi1 = u1;
                strTenDangNhap=strUser;
                strMatKhau=strPassWord;
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this,"Vui lòng đăng nhập lại","Lỗi thông tin",2);
            }
            con.close();
           
          
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

